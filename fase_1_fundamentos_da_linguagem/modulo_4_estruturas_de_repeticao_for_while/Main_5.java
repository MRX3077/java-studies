package fase_1_fundamentos_da_linguagem.modulo_4_estruturas_de_repeticao_for_while;

public class Main_5 {
    public static void main(String[] args){
        int i;

        for(i=0; i<=10; i=i+2){
            System.out.println("O valor de I("+i+") é menor ou igual a condição do FOR (Maior ou igual a 10).");
        }
        System.out.println("O valor de I("+i+") é maior que 10.");
    }
}
