package fase_1_fundamentos_da_linguagem.modulo_4_estruturas_de_repeticao_for_while;

public class Main_1 {
    public static void main(String[] args){
        int i=0;

        while(i<5){
            System.out.println("I("+i+") é menor que 5");
            i++;
        }
        System.out.println("I("+i+") é igual a 5");
    }
}
